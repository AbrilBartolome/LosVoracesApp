class Product{

    constructor(productId, name, category, isbn, price, author, section, stock, literaryGenre){
        this.productId = productId;
        this.name = name;
        this.category = category;
        this.author = author;
        this.isbn = isbn;
        this.section = section;
        this.price = price;
        this.stock = stock;
        this.literaryGenre = literaryGenre;

    }
}